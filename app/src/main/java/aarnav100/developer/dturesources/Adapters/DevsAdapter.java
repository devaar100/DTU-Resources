package aarnav100.developer.dturesources.Adapters;

import aarnav100.developer.dturesources.R;
import android.app.Activity;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ArrayAdapter;
import android.widget.ImageView;
import android.widget.TextView;

import java.util.ArrayList;

import aarnav100.developer.dturesources.Models.Devs;

/*
* {@link AndroidFlavorAdapter} is an {@link ArrayAdapter} that can provide the layout for each list
* based on a data source, which is a list of {@link AndroidFlavor} objects.
* */
public class DevsAdapter extends ArrayAdapter<Devs> {

    private static final String LOG_TAG = DevsAdapter.class.getSimpleName();

    /**
     * This is our own custom constructor (it doesn't mirror a superclass constructor).
     * The context is used to inflate the layout file, and the list is the data we want
     * to populate into the lists.
     *
     * @param context        The current context. Used to inflate the layout file.
     * @param androidFlavors A List of AndroidFlavor objects to display in a list
     */
    public DevsAdapter(Activity context, ArrayList<Devs> androidFlavors) {
        // Here, we initialize the ArrayAdapter's internal storage for the context and the list.
        // the second argument is used when the ArrayAdapter is populating a single TextView.
        // Because this is a custom adapter for two TextViews and an ImageView, the adapter is not
        // going to use this second argument, so it can be any value. Here, we used 0.
        super(context, 0, androidFlavors);
    }


    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        // Check if the existing view is being reused, otherwise inflate the view
        View listItemView = convertView;
        if(listItemView == null) {
            listItemView = LayoutInflater.from(getContext()).inflate(
                    R.layout.dev_list_item, parent, false);
        }

        Devs currentAndroidFlavor = getItem(position);


        TextView nameTextView = (TextView) listItemView.findViewById(R.id.version_name);

        nameTextView.setText(currentAndroidFlavor.getName());

        TextView numberTextView = (TextView) listItemView.findViewById(R.id.version_number);

        numberTextView.setText(currentAndroidFlavor.getMail());


        ImageView iconView = (ImageView) listItemView.findViewById(R.id.list_item_icon);

        iconView.setImageResource(currentAndroidFlavor.getImageResourceId());


        return listItemView;
    }

}
